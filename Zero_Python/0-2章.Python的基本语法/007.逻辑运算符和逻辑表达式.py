#逻辑运算符
'''
#逻辑运算符and
#逻辑表达式
#x and y
#描述：逻辑与， 当x为 True时才计算y  如果x就是False，那不管y是什么都是False
'''



"""
#逻辑运算符or
#逻辑表达式
#x || y
#描述：逻辑或， 当x为 True时才计算y  
"""
print (True or False)

print (False or True)
"""
#逻辑运算符not
#逻辑表达式
#not x
#描述：逻辑非
"""
#逻辑非的优先级大于逻辑与和逻辑或的优先级，逻辑与和逻辑或的优先级相等
#逻辑运算符的优先级低于关系运算符，必须先计算关系运算符，然后再计算逻辑运算符